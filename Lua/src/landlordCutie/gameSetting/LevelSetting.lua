function getLevelByExp(exp)
    for i=1,99 do
        if exp <= LevelSetting[i] then
            return i
        end
    end
    return 100
end

function getLevelInfo(exp)
    local levelInfo = {}
    if exp <= LevelSetting[1] then
        levelInfo.level = 1
        levelInfo.nextLevelExp = 100
        levelInfo.curNextLevelExp = exp
    elseif exp > LevelSetting[99] then
        levelInfo.level = 100
        levelInfo.nextLevelExp = 0
        levelInfo.curNextLevelExp = 0
    else
        levelInfo.level = getLevelByExp(exp)
        levelInfo.nextLevelExp = LevelSetting[levelInfo.level] - LevelSetting[levelInfo.level - 1]
        levelInfo.curNextLevelExp = exp - LevelSetting[levelInfo.level - 1]
    end
    return levelInfo
end